package com.b.operators.practise;

public class AssignmentOperator {

	public static void main(String[] args) {
		// It is used to assign the value on its right to the operand on its left
		
		int a=10;  
		a+=3;//10+3  
		System.out.println(a);  
		a-=4;//13-4  
		System.out.println(a);  
		a*=2;//9*2  
		System.out.println(a);  
		a/=2;//18/2  
		System.out.println(a);  
	

	}

}
